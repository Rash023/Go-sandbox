package main

import "fmt"

func main() {
	//string variable
	var name string = "John"
	//type inference
	var title = "Doe"
	//shorthand syntax
	middle := "Cena"

	fmt.Println(name)
	fmt.Println(middle)
	fmt.Println(title)

	var value int = 12
	fmt.Println(value)

	var flag bool = true
	fmt.Println(flag)

	var price float32 = 50.5
	fmt.Println(price)

}
